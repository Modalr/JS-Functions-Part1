function printHello(name, language) {
  if (language === "en") {
    console.log("Hello {$ name}");
  } else if (language === "es") {
    console.log("Hola {$ name}");
  } else if (language === "fr") {
    console.log("Bonjour {$ name}");
  } else if (language === "tr") {
    console.log("Merhaba ${name}");
  }
}

